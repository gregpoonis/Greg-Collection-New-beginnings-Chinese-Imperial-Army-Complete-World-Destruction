@dataclass
class ARVoxelCoordinate:
    """A position in Greg Pooni Multiverse space."""
    x: float
    y: float
    z: float
    dimension: ARDimensionality
    timestamp: str

    def euclidean_distance_to(self, other: "ARVoxelCoordinate") -> float:
        return np.sqrt((self.x - other.x)**2 + (self.y - other.y)**2 + (self.z - other.z)**2)

    def to_hyperbolic_projection(self) -> Dict[str, float]:
        """Project 3D Euclidean coords to Poincaré ball (meme science)."""
        norm = np.sqrt(self.x**2 + self.y**2 + self.z**2)
        if norm == 0:
            return {"u": 0.0, "v": 0.0, "w": 0.0}
        scale = 1.0 / (1.0 + norm)
        return {"u": self.x * scale, "v": self.y * scale, "w": self.z * scale}


@dataclass
class GregPooniEntity:
    """A sentient Greg Pooni instance existing in AR multiverse."""
    entity_id: str
    name: str
    quantum_state: QuantumState
    position: ARVoxelCoordinate
    probability_amplitude: float  # Between 0.0 and 1.0
    neural_fingerprint: str
    created_at: str
    metadata: Dict[str, Any]

    def collapse_wave_function(self) -> "GregPooniEntity":
        """Force quantum state collapse via observation."""
        self.quantum_state = QuantumState.COLLAPSED
        self.probability_amplitude = 1.0 if random.random() > 0.5 else 0.0
        return self


